num1 = 10
num2 = 25


# ARITHMETIC OPERATORs
print(" num1 + num2 is ", num1+num2)
print(" num1 - num2 is ", num1-num2)
print(" num1 * num2 is ", num1*num2)
print(" num1 / num2 is ", num1/num2)
print(" num1 // num2 is ", num1//num2)        #floor division, you'l not get digits after point
print(" num1 ** num2 is ", num1**num2)
print(" num1 % num2 is ", num1%num2)

